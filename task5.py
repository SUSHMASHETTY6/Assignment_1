
PI = 3.14
radius_of_the_circle = float(input('Enter the radius of the circle')) # user input

# Calculate the area of a circle and assign the value  to a variable name of _area_of_circle
_area_of_circle_ = PI * radius_of_the_circle * radius_of_the_circle

# Calculate the circumference of the circle and assign the value to a variable name of _circum_of_circle_

_circum_of_circle_ = 2 * PI * radius_of_the_circle
print('Area of the circle: ', _area_of_circle_)
print('Circumference of the circle: ', _circum_of_circle_)
